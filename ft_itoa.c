#include "libft.h"

char	*ft_itoa(int n)
{
	char	*str;
	int		i;
	int		a;

	a = ft_nblen(n);
	if (!(str = ft_strnew(a)))
		return (NULL);
	if (n == -2147483648)
		return (ft_strdup("-2147483648"));
	if (n == 0)
		str[0] = '0';
	if (n < 0)
	{
		n = -n;
		str[0] = '-';
	}
	i = a - 1;
	while (n != 0)
	{
		str[i--] = n % 10 + '0';
		n = n / 10;
	}
	return (str);
}
